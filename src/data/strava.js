function mapSegmentToXYZ(segment, flatten = false) {
	const R = 6371000; // Earth's radius in meters
	const startLat = segment.longitude[0][0];
	const startLon = segment.longitude[0][1];
	const startAlt = segment.altitude[0];

	return segment.longitude.map((coord, i) => {
		const lat = coord[0];
		const lon = coord[1];
		const alt = flatten ? 0 : segment.altitude[i];

		// Latitudinal distance (Y)
		const y = (lat - startLat) * (Math.PI / 180) * R;

		// Longitudinal distance (X) - adjusted for latitude
		const x =
			(lon - startLon) * (Math.PI / 180) * R * Math.cos((lat * Math.PI) / 180);

		// Relative Altitude (Z)
		const z = alt - startAlt;

		return { x: -x, y: z, z: y };
	});
}

const mapToXYZ = (data) => {
	const R_LAT = 111320; // Meters per degree latitude
	const originLat = data.longitude[0][0];
	const originLon = data.longitude[0][1];

	// Meters per degree longitude at this latitude
	const rLon = R_LAT * Math.cos(originLat * (Math.PI / 180));

	return data.longitude.map((coords, i) => {
		return {
			x: -1 * (coords[1] - originLon) * rLon, // Easting
			z: (coords[0] - originLat) * R_LAT, // Northing
			y: data.altitude[i], // Up
		};
	});
};

